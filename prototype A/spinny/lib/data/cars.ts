export function getFilteredCars(filters: {
  brands?: string[];
  bodyTypes?: string[];
  fuelTypes?: string[];
  transmissions?: string[];
  minPrice?: number;
  maxPrice?: number;
  sortBy?: string;
}): Car[] {
  let filtered = [...cars];

  if (filters.brands && filters.brands.length > 0) {
    filtered = filtered.filter(car => filters.brands!.includes(car.make));
  }
  if (filters.bodyTypes && filters.bodyTypes.length > 0) {
    filtered = filtered.filter(car => filters.bodyTypes!.includes(car.bodyType));
  }
  if (filters.fuelTypes && filters.fuelTypes.length > 0) {
    filtered = filtered.filter(car => filters.fuelTypes!.includes(car.fuelType));
  }
  if (filters.transmissions && filters.transmissions.length > 0) {
    filtered = filtered.filter(car => filters.transmissions!.includes(car.transmission));
  }
  if (filters.minPrice !== undefined) {
    filtered = filtered.filter(car => car.price >= filters.minPrice!);
  }
  if (filters.maxPrice !== undefined) {
    filtered = filtered.filter(car => car.price <= filters.maxPrice!);
  }

  if (filters.sortBy) {
    switch (filters.sortBy) {
      case 'price-low':
        filtered.sort((a, b) => a.price - b.price);
        break;
      case 'price-high':
        filtered.sort((a, b) => b.price - a.price);
        break;
      case 'year-new':
        filtered.sort((a, b) => b.year - a.year);
        break;
      case 'mileage-low':
        filtered.sort((a, b) => a.mileage - b.mileage);
        break;
    }
  }

  return filtered;
}
